    "    for (var di = 1; di < points; di++) {",
    "      if (balances[di] <= 0) {",
    "        var dx = xScale(di);",
    "        ctx.beginPath();",
    "        ctx.strokeStyle = 'rgba(243,156,18,0.6)';",
