    "  }",
    "",
    "  // Cumulative need line (dashed red)",
    "  ctx.beginPath();",
    "  ctx.strokeStyle = '#c0392b';",
    "  ctx.lineWidth   = 1.5;",
    "  ctx.setLineDash([4, 4]);",
    "  for (var ni = 0; ni < points; ni++) {",
    "    var nx = xScale(ni);",
    "    var ny = yScale(needs[ni]);",
    "    ni === 0 ? ctx.moveTo(nx, ny) : ctx.lineTo(nx, ny);",
    "  }",
    "  ctx.stroke();",
    "  ctx.setLineDash([]);",
    "",
    "  // Portfolio balance line (solid green)",
    "  ctx.beginPath();",
    "  for (var bi = 0; bi < points; bi++) {",
    "    var bx = xScale(bi);",
    "    var by = yScale(balances[bi]);",
    "    bi === 0 ? ctx.moveTo(bx, by) : ctx.lineTo(bx, by);",
    "  }",
    "  ctx.strokeStyle = '#27ae60';",
    "  ctx.lineWidth   = 2.5;",
    "  ctx.stroke();",
    "",
    "  // Fill under the portfolio balance line",
    "  ctx.beginPath();",
    "  for (var fi = 0; fi < points; fi++) {",
    "    var fx = xScale(fi);",
    "    var fy = yScale(balances[fi]);",
    "    fi === 0 ? ctx.moveTo(fx, fy) : ctx.lineTo(fx, fy);",
    "  }",
    "  ctx.lineTo(xScale(points - 1), pad.top + h);",
    "  ctx.lineTo(xScale(0),          pad.top + h);",
    "  ctx.closePath();",
    "  var grad = ctx.createLinearGradient(0, pad.top, 0, pad.top + h);",
    "  grad.addColorStop(0, 'rgba(39,174,96,0.15)');",
    "  grad.addColorStop(1, 'rgba(39,174,96,0)');",
    "  ctx.fillStyle = grad;",
    "  ctx.fill();",
    "",
    "  // Depletion marker (vertical dashed line + label)",
